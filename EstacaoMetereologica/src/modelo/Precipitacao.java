package modelo;

public class Precipitacao
{   
    private int id;
    private String chuva;
    private String neve;
    private String chuva_de_granizo;

    public int getId()
    {
        return id;
    }

    public void setId(int id)
    {
        this.id = id;
    }

    public String getChuva()
    {
        return chuva;
    }

    public void setChuva(String chuva)
    {
        this.chuva = chuva;
    }

    public String getNeve()
    {
        return neve;
    }

    public void setNeve(String neve)
    {
        this.neve = neve;
    }

    public String getChuva_de_granizo()
    {
        return chuva_de_granizo;
    }

    public void setChuva_de_granizo(String chuva_de_granizo)
    {
        this.chuva_de_granizo = chuva_de_granizo;
    }
    
    
}
